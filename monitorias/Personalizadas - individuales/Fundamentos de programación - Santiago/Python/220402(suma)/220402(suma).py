"""Programa que permite sumar dos numeros con while"""
otro = 1
while(otro==1):
        suma = 0
        a = int(input("Ingrese el primer termino:"))
        b = int(input("Ingrese el segundo termino:"))
        suma = a + b
        print ("El resultado de su suma es:", suma)
        otro = int(input("si desea otra suma introduzca 1:\n"))
